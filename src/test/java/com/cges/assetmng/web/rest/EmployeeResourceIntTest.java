    @Test
    @Transactional
    public void updateEmployee() throws Exception {
        // Initialize the database
        employeeRepository.saveAndFlush(employee);

        int databaseSizeBeforeUpdate = employeeRepository.findAll().size();

        // Update the employee
        Employee updatedEmployee = employeeRepository.findById(employee.getId()).get();
        // Disconnect from session so that the updates on updatedEmployee are not directly saved in db
        em.detach(updatedEmployee);
        updatedEmployee
            .name(UPDATED_NAME)
            .lastname(UPDATED_LASTNAME)
            .telNum1(UPDATED_TEL_NUM_1)
            .telNum2(UPDATED_TEL_NUM_2)
            .email(UPDATED_EMAIL)
            .startDate(UPDATED_START_DATE)
            .endDate(UPDATED_END_DATE)
            .pictureEmpl(UPDATED_PICTURE_EMPL)
            .notes(UPDATED_NOTES)
            .attachments(UPDATED_ATTACHMENTS);

        restEmployeeMockMvc.perform(put("/api/employees")
            .contentType(TestUtil.APPLICATION_JSON_UTF8)
            .content(TestUtil.convertObjectToJsonBytes(updatedEmployee)))
            .andExpect(status().isOk());

        // Validate the Employee in the database
        List<Employee> employeeList = employeeRepository.findAll();
        assertThat(employeeList).hasSize(databaseSizeBeforeUpdate);
        Employee testEmployee = employeeList.get(employeeList.size() - 1);
        assertThat(testEmployee.getName()).isEqualTo(UPDATED_NAME);
        assertThat(testEmployee.getLastname()).isEqualTo(UPDATED_LASTNAME);
        assertThat(testEmployee.getTelNum1()).isEqualTo(UPDATED_TEL_NUM_1);
        assertThat(testEmployee.getTelNum2()).isEqualTo(UPDATED_TEL_NUM_2);
        assertThat(testEmployee.getEmail()).isEqualTo(UPDATED_EMAIL);
        assertThat(testEmployee.getStartDate()).isEqualTo(UPDATED_START_DATE);
        assertThat(testEmployee.getEndDate()).isEqualTo(UPDATED_END_DATE);
        assertThat(testEmployee.getPictureEmpl()).isEqualTo(UPDATED_PICTURE_EMPL);
        assertThat(testEmployee.getNotes()).isEqualTo(UPDATED_NOTES);
        assertThat(testEmployee.getAttachments()).isEqualTo(UPDATED_ATTACHMENTS);
    }

